#!/bin/bash

# 条件1: 引数がちょうど2つあるかチェック
if [ $# -ne 2 ]; then
    echo "エラー: 引数は2つ指定してください。" 1>&2
    exit 1
fi

# 条件2: 1つ目の引数が「自然数（1以上の整数）」かチェック
if ! [[ "$1" =~ ^[1-9][0-9]*$ ]]; then
    echo "エラー: 1つ目の引数が自然数ではありません。" 1>&2
    exit 1
fi

# 条件3: 2つ目の引数が「自然数（1以上の整数）」かチェック
if ! [[ "$2" =~ ^[1-9][0-9]*$ ]]; then
    echo "エラー: 2つ目の引数が自然数ではありません。" 1>&2
    exit 1
fi

# echo "入力チェックを通過しました！ 引数1: $1, 引数2: $2"

# --- ここから計算ロジックを追加 ---

a=$1
b=$2

# bが0になるまでループを回す（ユークリッドの互除法）
while [ $b -ne 0 ]; do
    remainder=$((a % b))  # aをbで割った余りを計算
    a=$b                  # 次の計算のためにbの値をaに移す
    b=$remainder          # 余りの値をbに移す
done

# 最終的に a に残った値が最大公約数です
echo $a
